                    <h2 class="section-heading">¿Qué es la Biodiversidad?</h2>
                    <p>Diversidad biológica, o biodiversidad, se refiere a la diversidad de seres vivos que habitan el planeta y es resultado
                        de miles de millones de años de evolución de la vida en la Tierra.</p>
                    <p>El Convenio sobre Diversidad Biológica "CDB" la define de la siguiente manera:</p>

                    <blockquote class="blockquote">
                        <p class="mb-0">"Por diversidad biológica se entiende la variabilidad de organismos vivos de cualquier fuente, incluidos,
                            entre otras cosas, los ecosistemas terrestres y marinos y otros ecosistemas acuáticos y los complejos ecológicos de los que
                            forman parte; comprende la diversidad dentro de cada especie, entre las especies y de los ecosistemas"</p>
                    </blockquote>

                    <h2 class="section-heading">Niveles de la Biodiversidad</h2>
                    <p>La biodiversidad se estudia en tres distintos niveles, los cuales se describirán a continuación: </p>
                    <p>
                    <h3>Ecosistemas</h3>
                    <p>Es la variedad que existe, por ejemplo, de selvas, bosques, desiertos, manglares, tundras, arrecifes, costas, lagunas, ríos,
                        etc. </p><img class="img-fluid" src="assets/img/Biodiversity/Ecosistemas.png">
                    <span class="text-muted caption">Imagen de los diferentes ecosistemas en el mundo.</span>

                    <h3>Especies</h3>
                    <p>Son todos aquellos organismos que comparten características particulares. A la fecha se han identificado alrededor de 1.75
                        millones de especies, pero desconocemos aún a la mayoría, ya que se estima que en el planeta hay alrededor de 13 millones de
                        especies. </p>
                    <img class="img-fluid" src="assets/img/Biodiversity/Especies.jpg">
                    <span class="text-muted caption">Imagen de las distintas especies de animales en el mundo.</span>

                    <h3>Variación (dentro de las especies)</h3>
                    <p>Se refiere a las diferencias genéticas que existen entre organismos de la misma especie como pueden ser las diferencias entre
                        personas, entre caballos, entre maíces, entre perros, etc. </p><img class="img-fluid" src="assets/img/Biodiversity/Variación.png">
                    <span class="text-muted caption">Ejemplo de variación en tigres.</span>
                    </p>

                    <h2 class="section-heading">Importancia de la Biodiversidad</h2>
                    <p>La importancia de la biodiversidad se puede sintetizar en dos rasgos esenciales. Por un lado, es el fruto del trabajo de
                        millones de años de la naturaleza, por lo que su valor es incalculable e irremplazable. Por otro, es garantía para el f
                        uncionamiento
                        correcto del sistema que forman los seres vivos, junto con el medio en el que viven y al que contribuyen para su
                        supervivencia.</p>
                    <p>De esta forma, podemos afirmar que la biodiversidad no sólo es significativa para los seres humanos, sino que es esencial p
                        ara la
                        vida del planeta, por lo que debemos tratar de preservarla. La biodiversidad de las especies nos provee bienes tan necesarios
                        como el
                        alimento o el oxígeno, nos proporciona materias primas que favorecen el desarrollo económico, produce energía que utilizamos
                        como combustible,
                        es el origen de algunos medicamentos y, finalmente, pero no por ello menos importante, nos colma la retina de hermosos
                        paisajes que podemos disfrutar.</p>

                    <h2 class="section-heading">Biodiversidad en México</h2>
                    <p>El programa de Naciones Unidas para el Medio Ambiente (PNUMA) ha determinado que 12 países poseen una riqueza bilógica
                        sobresaliente,
                        en virtud de la cual han sido llamados “megadiversos”. Estos se encuentran principalmente en las zonas tropicales de
                        América y Asia: Australia,
                        Brasil, Costa Rica, Colombia, China, Ecuador, Nueva Guinea, México, Indonesia, Kenia, Papúa y Perú. Aunque su área
                        conjunta representa apenas
                        10% de la superficie terrestre, estos 12 países albergan cerca del 70% de las especies del planeta.</p>
                    <p>México es uno de los cuatro países con mayor diversidad biológica en el mundo, superado tan sólo por Brasil, Colombia e
                        Indonesia. En un territorio
                        que representa apenas 1.4% de la superficie de la Tierra, nuestro país alberga entre 10 y 12% de todas las especies
                        conocidas en el planeta. Pero la
                        importancia de la biodiversidad de México no sólo radica en este número, sino también en el hecho de que muchas de estas
                        especies son endémicas del país,
                        esto es, que sólo se encuentran en el territorio nacional.</p>
                    <p>Las cifras que se exponen a continuación permiten apreciar la enorme riqueza biológica de nuestro país y el gran capital biológico que poseemos:</p>
                    <li>El nuestro es el cuarto país más rico en especies de vertebrados terrestres (mamíferos, aves, reptiles y anfibios). Además, cuenta con 2,695 especies
                        de peces, que constituyen 10% de todas las descritas en el mundo.</li><a href="#">
                        <img class="img-fluid" src="assets/img/Biodiversity/Variación de vertebrados.png"></a>
                    <li>En México habitan 535 especies de mamíferos, 488 terrestres, y 47 marinas, número que sólo es superado por Indonesia y Brasil, con 667 y 578,
                        respectivamente.</li>
                    <img class="img-fluid" src="assets/img/Biodiversity/Variación de especies.png">
                    <li>En riqueza de aves, México ocupa el undécimo lugar mundial con 1050 especies conocidas, muchas de las cuales son endémicas. Esto hace de nuestro país un
                        importante centro de evolución y diversificación.</li>

                    <h2 class="section-heading">Especies animales emblemáticas de México en peligro de extinción</h2>
                    <h3>Vaquita marina</h3>
                    <p>La vaquita, o marsopa común del Golfo de California, es un mamífero marino endémico del Golfo de California y es la especie de cetáceo en mayor peligro de
                        extinción en el mundo. Las vaquitas son las más pequeñas de las siete marsopas conocidas. Las hembras crecen hasta 140 cm (4.6 pies) de largo, mientras que los
                        machos son un poco más pequeños, hasta 135 cm (4.4 pies).</p>
                    <img class="col-md-10 col-lg-8 mx-auto" src="assets/img/Biodiversity/Vaquita marina.jpg">

                    <p></p>
                    <h3>Tortuga Laúd</h3>
                    <p>Tan antigua como los dinosaurios, la tortuga laúd se encuentra en peligro de extinción. La tortuga laúd es el reptil más pesado del planeta y la más grande de
                        todas las tortugas vivientes, creciendo hasta 2 metros de largo y pesando hasta 700 kg. Se puede distinguir fácilmente de otras tortugas marinas por la falta de un
                        caparazón óseo o carapacho; en cambio, está cubierto por piel y carne aceitosa, lo que da lugar al nombre de “laúd”.</p>
                    <img class="img-fluid" src="assets/img/Biodiversity/TortugaLaud.jpg">

                    <p></p>
                    <h3>Lobo mexicano</h3>
                    <p>El lobo mexicano es la subespecie más pequeña de lobo gris en América del Norte. Es también uno de los mamíferos más raros
                        y en mayor en peligro de extinción en el continente, con sólo pocas docenas sobreviviendo en vida silvestre después de un
                        programa de reintroducción. </p>
                    <p>Alcanzan el tamaño de un pastor alemán, pesan entre 21 y 41 kg, y crecen entre 140 cm y 180 cm del hocico a la cola. Pueden
                        vivir hasta 8 años de edad en la naturaleza.</p>
                    <img class="img-fluid" src="assets/img/Biodiversity/LoboMexicano.jpg">

                    <p></p>
                    <h3>Ajolote Mexicano</h3>
                    <p>El ajolote mexicano es una salamandra larga, oscura, cilíndrica, midiendo hasta 30 cm (12 pulgadas) de largo. Esta criatura de
                        distintivo aspecto y en peligro de extinción tiene branquias grandes, que se extienden desde la parte posterior de su cabeza ancha.</p>
                    <p>En algún tiempo, el ajolote fue una fuente común de alimento. El ajolote también se utilizó en remedios populares que supuestamente
                        podrían tratar enfermedades respiratorias como el asma y la bronquitis. La creencia de sus propiedades terapéuticas, todavía
                        existe en algunos lugares, y hasta hace poco, fue posible encontrar bálsamos y jarabes en el mercado de Sonora en la Ciudad de
                        México que pretendía contener ajolote como ingrediente principal.</p>
                    <img class="img-fluid" src="assets/img/Biodiversity/Ajolote.jpg">

                    <p></p>
                    <h3>Guacamaya Roja</h3>
                    <p>Las guacamayas rojas son aves se distinguen por su colorido plumaje, con cuerpos de color rojo escarlata, plumas de las alas
                        amarillas y azul claro las plumas de la cola. </p>
                    <p>En México, la guacamaya roja ha sido completamente exterminada en los estados de Tamaulipas, Veracruz, Oaxaca, Tabasco y
                        Campeche. Actualmente, sólo hay dos poblaciones conocidas que habitan en menos del 5 por ciento del territorio mexicano. Estas
                        poblaciones se encuentran en la selva Lacandona, en el lado oriental de Chiapas y en Los Chimalapas, Oaxaca.</p>
                    <img class="img-fluid" src="assets/img/Biodiversity/Guacamaya.jpg">

                    <p></p>
                    <h3>Mariposa Monarca</h3>
                    <p>La monarca es tal vez la mariposa más conocida del mundo, pero ese estatus no le ha garantizado evitar las profundas amenazas
                        para su supervivencia. La gran migración de esta mariposa desde Estados Unidos al sur de Canadá a México y California en el
                        invierno, es uno de los más espectaculares y extraordinarios espectáculos naturales. </p>
                    <p>Las monarcas son mariposas relativamente grandes, midiendo 10 cm desde la punta de un ala a la punta la otra. Sus alas son
                        fáciles de reconocer - naranja brillante con rayas negras y manchas blancas. La parte inferior de las alas son de color naranja
                        pálido, por lo que cuando se pliegan sus alas en los árboles y otras plantas, las mariposas aparecen camufladas.</p>
                    <img class="img-fluid" src="assets/img/Biodiversity/MariposaMonarca.jpg">

                    <p></p>
                    <h3>Jaguar</h3>
                    <p>Conocido como “Océlotl” en náhuatl y “balam” en maya, el jaguar ha sido asociado en las diversas culturas de México con el
                        valor, poder, la noche, el inframundo, la fertilidad de la tierra y la muerte. </p>
                    <p>Los machos pueden llegar a pesar 150 kilogramos; las hembras son más pequeñas. En contraste con los otros felinos grandes,
                        el jaguar gruñe y rara vez ruge. Como depredadores, controlan las densidades poblacionales de sus presas, por lo tanto, la
                        desaparición de jaguares puede alterar ecosistemas enteros.</p>
                    <p>Hoy en día la especie se enfrenta a numerosas amenazas: la pérdida y fragmentación de su hábitat, la agricultura y la
                        ganadería, la caza, la reducción de sus presas (mamíferos, aves, reptiles y peces) y la expansión de los asentamientos
                        humanos.</p>
